package ex30;
public class ex30 {

    public static void main(String[] args) {

        for(int i=1; i<=12; i++)
        {
            for(int j=1;j<=12;j++)
            {
                System.out.print((j*i)+"\t");
            }
            System.out.println();
        }

        System.exit(0);
    }

}
